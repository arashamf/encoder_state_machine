uint8_t read_gray_code_from_encoder(void ) 
{ 
  volatile uint8_t value = 0; 
  volatile uint8_t flag = 1;
  if((flag = LL_GPIO_IsInputPinSet(ENC_IN1_GPIO_Port, ENC_IN1_Pin)) == 0) 
	{ 
    value |= (1<<1); 
  }

  if((flag = LL_GPIO_IsInputPinSet(ENC_IN2_GPIO_Port, ENC_IN2_Pin)) == 0) 
	{ 
    value |= (1<<0); 
  }

  return value; 
}
